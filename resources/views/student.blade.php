    <script type="text/javascript">
      $(document).ready(function(){
        $("#search_txt").keyup(function() {
          var txt = $(this).val();

          if (txt !='') {

          }else {
            $("#result").html('');

            $.ajax({
              url: '/result',
              method: 'post',
              dataType: 'text',
              data: {search: 'txt'},
              success:function(data){
                $("#result").html(data);
              }
            })
          }
        });
      });
